function escapeHtml(str=''){
return str.replace(/[&<>"']/g,s=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[s]))
}

function bindGlobal(){
document.getElementById('themeToggle').onclick=()=>setTheme(document.body.classList.contains('light')?'dark':'light')
document.getElementById('navBurger').onclick=()=>document.getElementById('navMenu').classList.toggle('open')
document.getElementById('modalBackdrop').onclick=closeModal
document.getElementById('modalClose').onclick=closeModal
window.addEventListener('hashchange',renderRoute)
window.addEventListener('scroll',()=>{
document.querySelectorAll('.parallax').forEach(el=>{
const speed=Number(el.dataset.speed||0.1)
el.style.setProperty('--parallax',`${window.scrollY*speed}px`)
})
})
}
function initReveal(){
const items=document.querySelectorAll('.reveal')
const io=new IntersectionObserver(entries=>{
entries.forEach(entry=>{
if(entry.isIntersecting) entry.target.classList.add('visible')
})
},{threshold:.12})
items.forEach(item=>io.observe(item))
}
function setActiveNav(){
const route=getRoute().path
document.querySelectorAll('.nav-menu a').forEach(a=>{
a.classList.toggle('active',a.getAttribute('href')===`#${route}`)
})
}
function renderRoute(){
detailImageIndex=0
const {path}=getRoute()
setActiveNav()
if(path==='/'||path==='') renderHome()
else if(path==='/catalog') renderCatalog()
else if(path.startsWith('/university/')) renderUniversity(path.split('/').pop())
else if(path==='/compare') renderComparePage()
else if(path==='/scholarships') renderScholarshipsPage()
else if(path==='/countries') renderCountriesPage()
else if(path==='/how-to-apply') renderHowToApplyPage()
else if(path==='/dashboard') renderDashboardPage()
else if(path==='/favorites') renderFavoritesPage()
else if(path==='/deadlines') renderDeadlinesPage()
else app.innerHTML=pageWrap('Страница не найдена','Такого маршрута нет.',`<div class="empty"><a href="#/">Вернуться на главную</a></div>`)
initReveal()
document.getElementById('navMenu').classList.remove('open')
}
