function scoreUniversity(u,filters={}){
let score=52
const reasons=[]
if(filters.query){
const q=filters.query.toLowerCase()
const hit=[u.name,u.country,u.city,u.major,u.language,u.region].join(' ').toLowerCase().includes(q)
if(hit){
score+=12
reasons.push('Совпал с поисковым запросом')
}
}
if(filters.country&&u.country===filters.country){
score+=18
reasons.push(`Подходит по стране: ${u.country}`)
}
if(filters.region&&u.region===filters.region){
score+=12
reasons.push(`Подходит по региону: ${u.region}`)
}
if(filters.language&&u.language===filters.language){
score+=14
reasons.push(`Есть обучение на ${u.language}`)
}
if(filters.major&&u.major===filters.major){
score+=20
reasons.push(`Подходит по специальности: ${u.major}`)
}
if(filters.grant&&u.grant===filters.grant){
score+=10
reasons.push(filters.grant==='Yes'?'Есть грант':'Без гранта')
}
if(filters.maxBudget){
if(u.budget<=Number(filters.maxBudget)){
score+=15
reasons.push(`Вписывается в бюджет до $${filters.maxBudget}`)
}else{
score-=18
}
}
if(filters.status&&getStatuses()[u.slug]===filters.status){
score+=8
reasons.push(`Текущий статус: ${filters.status}`)
}
score+=Math.max(0,25-Math.min(25,Math.floor(u.rank/6)))
if(u.housing==='Yes'){
score+=4
if(reasons.length<4) reasons.push('Есть общежитие')
}
return {score:Math.max(1,Math.min(99,score)),reasons:reasons.slice(0,4)}
}
function filterUniversities(filters={}){
let items=universities.map(u=>({u,...scoreUniversity(u,filters)}))
items=items.filter(item=>{
const u=item.u
if(filters.query){
const q=filters.query.toLowerCase()
if(![u.name,u.country,u.city,u.major,u.language,u.region].join(' ').toLowerCase().includes(q)) return false
}
if(filters.country&&u.country!==filters.country) return false
if(filters.region&&u.region!==filters.region) return false
if(filters.language&&u.language!==filters.language) return false
if(filters.major&&u.major!==filters.major) return false
if(filters.grant&&u.grant!==filters.grant) return false
if(filters.maxBudget&&u.budget>Number(filters.maxBudget)) return false
return true
})
const sortBy=filters.sortBy||'score'
items.sort((a,b)=>{
if(sortBy==='rank') return a.u.rank-b.u.rank
if(sortBy==='budgetLow') return a.u.budget-b.u.budget
if(sortBy==='budgetHigh') return b.u.budget-a.u.budget
if(sortBy==='name') return a.u.name.localeCompare(b.u.name)
if(sortBy==='chance') return b.score-a.score
return b.score-a.score
})
return items
}
function calcCountryStats(){
const map={}
universities.forEach(u=>{
if(!map[u.country]) map[u.country]={count:0,budget:0,bestRank:999}
map[u.country].count++
map[u.country].budget+=u.budget
map[u.country].bestRank=Math.min(map[u.country].bestRank,u.rank)
})
return Object.entries(map).map(([country,v])=>({
country,
count:v.count,
avgBudget:Math.round(v.budget/v.count),
bestRank:v.bestRank
})).sort((a,b)=>a.bestRank-b.bestRank)
}
